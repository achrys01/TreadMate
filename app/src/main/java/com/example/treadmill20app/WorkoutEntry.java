package com.example.treadmill20app;

public class WorkoutEntry {
    private float dur;
    private float durStep;
    private float speed;
    private float speedStep;
    private float incl;
    private float inclStep;

    public void setDur(float durEntry){dur = durEntry;}
    public void setSpeed(float speedEntry){speed = speedEntry;}
    public void setIncl(float inclEntry){incl = inclEntry;}

    public float getDur(){
        return durStep = dur;
    }
    public float getSpeed(){
        return speedStep = speed;
    }
    public float getIncl(){
        return inclStep = incl;
    }
}
